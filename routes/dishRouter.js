const express = require('express');
const bodyParser = require('body-parser');
const mongoose = require('mongoose');

const Dishes = require('../models/dishes');

const dishRouter = express.Router();

dishRouter.use(bodyParser.json());

// We need to mount to this expressRouter in my index.js file
// So, In index.js, we will mount this Express router / (slash)
// dishes endpoint.
// ---
// This group is one single unit implemented by using the dishRouter
// on this particular router.
dishRouter.route('/')
.get((req, res, next) => {
    Dishes.find({})     // return a promise
    .then((dishes) => { // If the promise is resolved correctly, handler returned promise in here
        res.statusCode = 200;
        res.setHeader('Content-Type', 'application/json');
        res.json(dishes);   // put dishes into the body of reply message
                            // then send it back to the server
    }, (err) => next(err))
    .catch((err) => next (err));
})
.post((req, res, next) => {
    Dishes.create(req.body)
    .then((dish) => {
        console.log('Dish Created ', dish);
        res.statusCode = 200;
        res.setHeader('Content-Type', 'application/json');
        res.json(dish);
    }, (err) => next(err))
    .catch((err) => next(err));
})
.put((req, res, next) => {
    res.statusCode = 403;
    res.end('PUT operation not supported on /dishes');
})
.delete((req, res, next) => {
    Dishes.remove({})
    .then((resp) => {
        res.statusCode = 200;
        res.setHeader('Content-Type', 'application/json');
        res.json(resp);
    }, (err) => next(err))
    .catch((err) => next(err));
});

// dishId
